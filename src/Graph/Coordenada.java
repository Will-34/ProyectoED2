package Graph;

/**
 *
 * @author richardpg
 */
public class Coordenada {

    public int x;
    public int y;

    public Coordenada() {

    }

    public Coordenada(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
